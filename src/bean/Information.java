package bean;

public class Information {

	private String info_id;
	private String info_title;
	private String info_main;
	private String facility_id;
	private String info_genre;
	private String info_date;

	public String getInfo_id() {
		return info_id;
	}
	public void setInfo_id(String info_id) {
		this.info_id = info_id;
	}
	public String getInfo_title() {
		return info_title;
	}
	public void setInfo_title(String info_title) {
		this.info_title = info_title;
	}
	public String getInfo_main() {
		return info_main;
	}
	public void setInfo_main(String info_main) {
		this.info_main = info_main;
	}
	public String getFacility_id() {
		return facility_id;
	}
	public void setFacility_id(String facility_id) {
		this.facility_id = facility_id;
	}
	public String getInfo_genre() {
		return info_genre;
	}
	public void setInfo_genre(String info_genre) {
		this.info_genre = info_genre;
	}
	public String getInfo_date() {
		return info_date;
	}
	public void setInfo_date(String info_date) {
		this.info_date = info_date;
	}




}
